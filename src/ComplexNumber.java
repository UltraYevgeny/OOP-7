public class ComplexNumber{
    private int realArg;
    private int imagArg;

    public int getRealArg() {
        return realArg;
    }
    public void setRealArg(int realArg) {
        this.realArg = realArg;
    }
    public int getImagArg() {
        return imagArg;
    }
    public void setImagArg(int imagArg) {
        this.imagArg = imagArg;
    }
}
